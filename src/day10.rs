const DAY: i32 = 10;

enum Op {
    AddX(i32),
    Noop,
}

fn parse_instruction(line: &str) -> Op {
    let mut pieces = line.split(" ");
    match pieces.next().unwrap() {
        "noop" => Op::Noop,
        "addx" => Op::AddX(pieces.next().unwrap().parse().unwrap()),
        _ => unreachable!(),
    }
}

